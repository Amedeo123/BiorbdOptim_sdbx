plt.plot(np.arange(len(Nmhe)-1), np.tile(err_q[-1], (len(Nmhe)-1, 1)), '--',  label='err. full window')
fig.set_xticks(range(len(Nmhe)-1))
fig.set_xticklabels(Nmhe[:-1])
plt.ylabel('q err. (rad)')
plt.xlabel('Size of MHE window')
plt.legend(loc=0)

fig = plt.subplot(512)

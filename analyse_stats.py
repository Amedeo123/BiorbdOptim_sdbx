import pandas as pd
import matplotlib.pyplot as plt
import numpy as np

data = pd.read_csv("solutions/stats_mult_th.txt", delimiter=';')
data1th = pd.read_csv("solutions/stats_1_th.txt", delimiter=';')
data_sorted = data.sort_values('Nmhe')
data_sorted1th = data1th.sort_values('Nmhe')

Nmhe = np.array(data_sorted['Nmhe'])
time = np.array(data_sorted['time'])
time1th = np.array(data_sorted1th['time'])
err_q = np.array(data_sorted['err_q'])
err_dq = np.array(data_sorted['err_dq'])
err_tau = np.array(data_sorted['err_tau'])
err_muscles = np.array(data_sorted['err_muscles'])
err_markers = np.array(data_sorted['err_markers'])

fig = plt.subplot()
plt.plot(1/time[:-1], label='mhe multi thread')
plt.plot(1/time1th, label='mhe one thread')
plt.plot(np.arange(len(Nmhe)-1), np.tile(1/0.075, (len(Nmhe)-1, 1)), '--',  label='biofeedback standard')
fig.set_xticks(range(len(Nmhe)-1))
fig.set_xticklabels(Nmhe[:-1])
plt.legend()
plt.ylabel('Freq. (Hz)')
plt.xlabel('Size of MHE window')

fig=plt.figure()
fig = plt.subplot(511)
